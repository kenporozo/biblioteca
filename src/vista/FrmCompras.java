    private void cargarDistribuidores() {
        DefaultComboBoxModel items = new DefaultComboBoxModel();
        ArrayList<Object> lista = dDAO.getList();

        for (Object obj : lista) {
            Distribuidor dis = (Distribuidor) obj;
            items.addElement(dis);
        }
        cboDistribuidor.setModel(items);
    }

