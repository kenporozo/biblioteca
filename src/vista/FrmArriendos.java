    private void listar() {
        ArrayList<Object> lista = aDAO.getListArriendosPend();
        DefaultTableModel modeloTabla;

        String[] titulos = {"ID Boleta", "Trabajador", "Cliente", "Precio neto", "Precio IVA", "Fecha arriendo", "Fecha estimada de devolucion", "Libro"};
        modeloTabla = new DefaultTableModel(null, titulos);

        for (Object obj : lista) {
            Arriendo arriendo = (Arriendo) obj;
            Object[] row = new Object[8];
            row[0] = arriendo.getFactura().getIdFactura();
            row[1] = arriendo.getTrabajador().getNombre();
            row[2] = arriendo.getCliente().getNombre();
            row[3] = "$" + arriendo.getFactura().getPrecioNeto();
            row[4] = "$" + arriendo.getFactura().getPrecioIva();
            row[5] = arriendo.getFactura().getFechaCompra();
            row[6] = arriendo.getFactura().getFechaEstimada();
            row[7] = arriendo.getLibro().getTitulo();
            modeloTabla.addRow(row);
        }
        tablaListadoPendientes.setModel(modeloTabla);
        tabGeneral.setEnabledAt(1, false);
    }

