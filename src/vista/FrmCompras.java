    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        if (tablaListadoCompras.getSelectedRowCount() == 1) {
            String id = String.valueOf(tablaListadoCompras.getValueAt(tablaListadoCompras.getSelectedRow(), 0));

            if (JOptionPane.showConfirmDialog(this, "Deseas eliminar la compra #: " + id + " ?", "Desactivar", JOptionPane.YES_NO_OPTION) == 0) {

                if (cDAO.eliminar(Integer.parseInt(id))) {
                    this.mensajeOk("Registro eliminado");
                    this.listar();
                } else {
                    this.mensajeError("Error, el regristro no pudo ser eliminado");
                }
            }

        } else {
            this.mensajeError("Seleccione 1 registro a activar.");
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

