    private void listar() {
        ArrayList<Object> lista = vDAO.getList();
        DefaultTableModel modeloTabla;

        String[] titulos = {"ID", "Trabajador", "Cliente", "ID boleta", "Precio neto", "Precio IVA", "Fecha compra", "Tipo de pago", "Libro"};
        modeloTabla = new DefaultTableModel(null, titulos);

        for (Object obj : lista) {
            Venta venta = (Venta) obj;
            Object[] row = new Object[9];
            row[0] = venta.getIdVenta();
            row[1] = venta.getTrabajador().getNombre();
            row[2] = venta.getCliente().getNombre();
            row[3] = venta.getFactura().getIdFactura();
            row[4] = "$" + venta.getFactura().getPrecioNeto();
            row[5] = "$" + venta.getFactura().getPrecioIva();
            row[6] = venta.getFactura().getFechaCompra();
            switch (venta.getFactura().getIdPago()) {
                case 1:
                    row[7] = "Efectivo";
                    break;
                case 2:
                    row[7] = "Debito";
                    break;
                case 3:
                    row[7] = "Credito";
                    break;
                default:
                    throw new AssertionError();
            }
            row[8] = venta.getLibro().getTitulo();
            modeloTabla.addRow(row);
        }
        tablaListadoVentas.setModel(modeloTabla);
        tabGeneral.setEnabledAt(1, false);
    }

