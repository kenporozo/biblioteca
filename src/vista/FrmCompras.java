    private void listar() {
        ArrayList<Object> lista = cDAO.getList();
        DefaultTableModel modeloTabla;

        String[] titulos = {"ID", "Empresa", "ID Factura", "Precio neto", "Precio IVA", "Fecha compra", "Tipo de pago", "Detalle"};
        modeloTabla = new DefaultTableModel(null, titulos);

        for (Object obj : lista) {
            Compra compra = (Compra) obj;
            Object[] row = new Object[8];
            row[0] = compra.getIdCompra();
            row[1] = compra.getDistribuidor().getNombreDis();
            row[2] = compra.getFactura().getIdFactura();
            row[3] = "$" + compra.getFactura().getPrecioNeto();
            row[4] = "$" + compra.getFactura().getPrecioIva();
            row[5] = compra.getFactura().getFechaCompra();
            switch (compra.getFactura().getIdPago()) {
                case 1:
                    row[6] = "Efectivo";
                    break;
                case 2:
                    row[6] = "Debito";
                    break;
                case 3:
                    row[6] = "Credito";
                    break;
                default:
                    throw new AssertionError();
            }
            row[7] = compra.getFactura().getDetalleCompra();
            modeloTabla.addRow(row);
        }
        tablaListadoCompras.setModel(modeloTabla);
        tabGeneral.setEnabledAt(1, false);
    }

