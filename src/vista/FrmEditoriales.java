    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        Editorial edit;
        if (txtEditorial.getText().length() == 0) {
            JOptionPane.showMessageDialog(this, "Por favor, ingrese un nombre válido", "System", JOptionPane.WARNING_MESSAGE);
            txtEditorial.requestFocus();
            return;
        }

        if (this.accion.equals("editar")) {
            int id = Integer.parseInt(txtId.getText());
            String editorial = txtEditorial.getText();
            edit = new Editorial(id, editorial);
            if(eDAO.modificar(edit)){
                this.mensajeOk("Editorial actualizada existosamente");
                this.limpiar();
                this.listar();
                tabGeneral.setEnabledAt(0, true);
                tabGeneral.setEnabledAt(1, false);
                tabGeneral.setSelectedIndex(0);
            }else{
                this.mensajeError("Ocurrió un error, la editorial no fue actualizada");
            }

        } else {
            String editorial = txtEditorial.getText();
            edit = new Editorial(editorial);
            if (eDAO.insertar(edit)) {
                this.mensajeOk("Editorial añadida existosamente");
                this.limpiar();
                this.listar();
                tabGeneral.setEnabledAt(0, true);
                tabGeneral.setEnabledAt(1, false);
                tabGeneral.setSelectedIndex(0);
            } else {
                this.mensajeError("La editorial que intentas añadir, ya existe");
            }
        }
    }//GEN-LAST:event_btnGuardarActionPerformed


