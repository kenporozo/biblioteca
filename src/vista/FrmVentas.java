    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        Cliente cliente = (Cliente) cboClientes.getSelectedItem();
        Libro libro = (Libro) cboLibros.getSelectedItem();
        int idPago = cboTipoPago.getSelectedIndex();
        int id;
        switch (idPago) {
            case 0 ->
                id = 1;
            case 1 ->
                id = 2;
            case 2 ->
                id = 3;
            default ->
                throw new AssertionError();
        }
        Factura factura = new Factura(libro.getPrecio(), id);
        Venta venta = new Venta(factura, FrmLogin.user.getTrabajador(), cliente, libro);
        if (vDAO.insertar(venta)) {
            this.mensajeOk("Venta registrada existosamente");
            this.limpiar();
            this.listar();
            tabGeneral.setEnabledAt(0, true);
            tabGeneral.setEnabledAt(1, false);
            tabGeneral.setSelectedIndex(0);
        } else {
            this.mensajeError("Ocurrio un error al intentar registrar la venta");
        }
    }//GEN-LAST:event_btnGuardarActionPerformed


