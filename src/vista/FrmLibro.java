    private void listar() {
        ArrayList<Object> lista = lDAO.getList();
        DefaultTableModel modeloTabla;

        String[] titulos = {"ID", "ISBN", "Titulo", "#Paginas", "Año de publicacion", "Precio", "Idioma", "Editorial", "Categoria", "Autor", "Estado"};
        modeloTabla = new DefaultTableModel(null, titulos);

        for (Object obj : lista) {
            Libro libro = (Libro) obj;
            Object[] row = new Object[11];
            row[0] = libro.getIdLibro();
            row[1] = libro.getIsbn();
            row[2] = libro.getTitulo();
            row[3] = libro.getCantidadPaginas();
            row[4] = libro.getFechaPublicacion();
            row[5] = libro.getPrecio();
            row[6] = libro.getIdioma().getIdioma();
            row[7] = libro.getEditorial().getNombreEditorial();
            row[8] = libro.getCategoria().getNombreCategoria();
            row[9] = libro.getAutor().getNombreAutor();
            switch (libro.getEstado()) {
                case 1:
                    row[10] = "Vendido";
                    break;
                case 2:
                    row[10] = "Arrendado";
                    break;
                case 3:
                    row[10] = "Disponible";
                    break;
                case 4:
                    row[10] = "No disponible";
                    break;
                default:
                    throw new AssertionError();
            }
            modeloTabla.addRow(row);
        }
        tablaListadoLibros.setModel(modeloTabla);
        tabGeneral.setEnabledAt(1, false);
    }

