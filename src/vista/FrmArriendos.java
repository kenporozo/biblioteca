    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed

        if (txtFecha.getText().length() == 0) {
            JOptionPane.showMessageDialog(this, "Por favor, ingrese una fecha v√°lida", "System", JOptionPane.WARNING_MESSAGE);
            txtFecha.requestFocus();
            return;
        }

        if (this.accion.equals("guardar")) {
            Cliente cliente = (Cliente) cboClientes.getSelectedItem();
            Libro libro = (Libro) cboLibros.getSelectedItem();
            String fechaEstimada = txtFecha.getText();
            Factura factura = new Factura(libro.getPrecio(), fechaEstimada);
            Arriendo arriendo = new Arriendo(FrmLogin.user.getTrabajador(), cliente, factura, libro);
            if (aDAO.insertar(arriendo)) {
                this.mensajeOk("Arriendo registrado existosamente");
                this.limpiar();
                this.listar();
                tabGeneral.setEnabledAt(0, true);
                tabGeneral.setEnabledAt(1, false);
                tabGeneral.setSelectedIndex(0);
            } else {
                this.mensajeError("Ocurrio un error al intentar registrar el arriendo");
            }
        } else {
            int id = Integer.parseInt(txtId.getText());
            String fecha = txtFecha.getText();
            Arriendo arriendo = (Arriendo) aDAO.buscar(id);
            int idPago = cboTipoPago.getSelectedIndex();
            int idCbo;
            switch (idPago) {
                case 0 ->
                    idCbo = 1;
                case 1 ->
                    idCbo = 2;
                case 2 ->
                    idCbo = 3;
                default ->
                    throw new AssertionError();
            }
            arriendo.getFactura().setIdPago(idCbo);
            Arriendo ar = new Arriendo(arriendo.getIdArriendo(), arriendo.getTrabajador(), arriendo.getCliente(), arriendo.getFactura(), arriendo.getLibro(), fecha);
            if (aDAO.insertarArriendo(ar)) {
                int diasPasados = aDAO.buscarMulta(id);
                int multa = 1500;
                if (diasPasados > 0) {
                    if (aDAO.insertarMulta(id, ((diasPasados * multa) + ar.getLibro().getPrecio()))) {
                        this.mensajeOk("Tienes una multa de " + ((diasPasados * multa) + ar.getLibro().getPrecio()) + " por pagar");
                        this.limpiar();
                        this.listar();
                        tabGeneral.setEnabledAt(0, true);
                        tabGeneral.setEnabledAt(1, false);
                        tabGeneral.setSelectedIndex(0);
                    }
                } else {
                    this.mensajeOk("Arriendo pagado existosamente");
                    this.limpiar();
                    this.listar();
                    tabGeneral.setEnabledAt(0, true);
                    tabGeneral.setEnabledAt(1, false);
                    tabGeneral.setSelectedIndex(0);
                }
            }
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

    private void btnPagarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPagarActionPerformed
        if (tablaListadoPendientes.getSelectedRowCount() == 1) {
            String id = String.valueOf(tablaListadoPendientes.getValueAt(tablaListadoPendientes.getSelectedRow(), 0));

            txtId.setText(id);

            tabGeneral.setEnabledAt(0, false);
            tabGeneral.setEnabledAt(1, true);
            tabGeneral.setSelectedIndex(1);
            lblTipoPago.setVisible(true);
            cboTipoPago.setVisible(true);
            lblLibro.setVisible(false);
            lblClientes.setVisible(false);
            cboClientes.setVisible(false);
            cboLibros.setVisible(false);
            lblFecha.setText("Fecha entrega");
            this.accion = "pagar";
            btnGuardar.setText("Pagar");

        } else {
            this.mensajeError("Seleccione 1 registro a editar.");
        }
    }//GEN-LAST:event_btnPagarActionPerformed

