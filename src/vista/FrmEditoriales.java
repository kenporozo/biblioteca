    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        if (tablaListadoEditorial.getSelectedRowCount() == 1) {
            String id = String.valueOf(tablaListadoEditorial.getValueAt(tablaListadoEditorial.getSelectedRow(), 0));
            String editorial = String.valueOf(tablaListadoEditorial.getValueAt(tablaListadoEditorial.getSelectedRow(), 1));

            txtId.setText(id);
            txtEditorial.setText(editorial);

            tabGeneral.setEnabledAt(0, false);
            tabGeneral.setEnabledAt(1, true);
            tabGeneral.setSelectedIndex(1);
            this.accion = "editar";
            btnGuardar.setText("Editar");

        } else {
          this.mensajeError("Seleccione 1 registro a editar.");
        }
    }//GEN-LAST:event_btnEditarActionPerformed

