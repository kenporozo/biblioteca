    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        if (txtPrecio.getText().length() == 0) {
            JOptionPane.showMessageDialog(this, "Por favor, ingrese un precio válido", "System", JOptionPane.WARNING_MESSAGE);
            txtPrecio.requestFocus();
            return;
        }
        if (txpDetalle.getText().length() == 0 || txpDetalle.getText().length() > 100) {
            JOptionPane.showMessageDialog(this, "Por favor, ingrese un detalle de compra válido que no supere los 100 caracteres válido", "System", JOptionPane.WARNING_MESSAGE);
            txpDetalle.requestFocus();
            return;
        }

        Double precio = Double.parseDouble(txtPrecio.getText());
        String detalle = txpDetalle.getText();
        int idPago = cboTipoPago.getSelectedIndex();
        Distribuidor distribuidor = (Distribuidor) cboDistribuidor.getSelectedItem();
        int id;
        switch (idPago) {
            case 0 ->
                id = 1;
            case 1 ->
                id = 2;
            case 2 ->
                id = 3;
            default ->
                throw new AssertionError();
        }

        Factura factura = new Factura(precio, id, detalle);
        Compra compra = new Compra(distribuidor, factura);

        if (cDAO.insertar(compra)) {
            this.mensajeOk("Compra registrada existosamente");
            this.limpiar();
            this.listar();
            tabGeneral.setEnabledAt(0, true);
            tabGeneral.setEnabledAt(1, false);
            tabGeneral.setSelectedIndex(0);
        } else {
            this.mensajeError("Ocurrio un error al intentar registrar la compra");
        }
    }//GEN-LAST:event_btnGuardarActionPerformed


